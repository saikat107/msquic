TEST(RangeTest, DeepTest_CompactTriggersActualShrink)
{
    // Force a scenario where Compact will actually call QuicRangeShrink
    SmartRange range;
    
    // Add many ranges to grow allocation significantly
    for (uint32_t i = 0; i < 200; i += 2) {
        range.Add(i);
    }
    uint32_t originalAlloc = range.range.AllocLength;
    ASSERT_GT(originalAlloc, QUIC_RANGE_INITIAL_SUB_COUNT * 4);
    
    // Remove most ranges to trigger shrink threshold
    for (uint32_t i = 0; i < 195; i += 2) {
        range.Remove(i, 1);
    }
    
    // Should have just a few ranges left
    uint32_t usedAfterRemoval = range.ValidCount();
    ASSERT_LE(usedAfterRemoval, (uint32_t)3);
    
    // Now call Compact which should trigger shrinking due to low usage
    uint32_t allocBeforeCompact = range.range.AllocLength;
    QuicRangeCompact(&range.range);
    
    // Allocation may have shrunk if threshold was met
    // (threshold: AllocLength >= INITIAL * 4 AND UsedLength < AllocLength / 8)
    if (allocBeforeCompact >= QUIC_RANGE_INITIAL_SUB_COUNT * 4 &&
        usedAfterRemoval < allocBeforeCompact / 8) {
        ASSERT_LT(range.range.AllocLength, allocBeforeCompact);
    }
}

